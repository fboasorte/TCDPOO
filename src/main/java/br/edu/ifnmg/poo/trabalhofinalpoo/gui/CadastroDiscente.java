    private void limparCampos() {
        txtCpf.setText(null);
        txtNome.setText(null);
        txtNascimento.setText(null);

        txtCpf.requestFocus();
    }

    void atualizarModelo(Discente discente) {
        lstDiscentesModel.set(indiceDiscenteSelecionado, discente);
    }

